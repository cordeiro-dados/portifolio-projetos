function openModalLogistica(event) {
  // Previne o comportamento padrão (subir a página)
  event.preventDefault();

  // Abre o modal
  document.getElementById("modal_logistica").style.display = "block";
}



function closeModalLogistica() {
  document.getElementById("modal_logistica").style.display = "none";
}


